package db;

public class Os {

	public String product_id;
	public String os_system;
	public String os_source;
	public String os_version;
	
	public String getProduct_id() {
		return product_id;
	}
	public void setProduct_id(String product_id) {
		this.product_id = product_id;
	}
	public String getOs_system() {
		return os_system;
	}
	public void setOs_system(String os_system) {
		this.os_system = os_system;
	}
	public String getOs_source() {
		return os_source;
	}
	public void setOs_source(String os_source) {
		this.os_source = os_source;
	}
	public String getOs_version() {
		return os_version;
	}
	public void setOs_version(String os_version) {
		this.os_version = os_version;
	}
	
	
}

package db;

public class Processor {

	public String product_id;
	public String processor_brand;
	public String processor_name;
	public String speed_GHz;
	public String getProduct_id() {
		return product_id;
	}
	public void setProduct_id(String product_id) {
		this.product_id = product_id;
	}
	public String getProcessor_brand() {
		return processor_brand;
	}
	public void setProcessor_brand(String processor_brand) {
		this.processor_brand = processor_brand;
	}
	public String getProcessor_name() {
		return processor_name;
	}
	public void setProcessor_name(String processor_name) {
		this.processor_name = processor_name;
	}
	public String getSpeed_GHz() {
		return speed_GHz;
	}
	public void setSpeed_GHz(String speed_GHz) {
		this.speed_GHz = speed_GHz;
	}
	
	
}
